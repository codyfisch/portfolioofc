        <h3>Background</h3>

        <p>I grew up in North Jersey, got a BA in Neuroscience in Pennsylvania and an MS in Physiology in New York.
           So where does the web development fit in? Everything I have
        learned about creating web pages was made possible by a slew of wonderful instructional websites
         and by books checked out from the local public library.</p>

        <p>After spending a lot of time searching around for jobs in the research bio-sciences without much success, I began turning
        toward other possibilities for myself. As a long-standing avid consumer of web-based content, it wasn't much of a stretch for me to
        start looking toward web development as a possible
        career path. And thus began my pursuit of web-enlightenment...</p>

        <p>In April of 2010, I learned
        what HTML stood for. In June 2010, Portfolioofc.com (Version 1.0) was born. Since then, I've been adding
        functionality to the site as I learned the techniques needed to make a robust web experience. In a few short
        months I went from cobbling pages together using Notepad to producing dynamic content with PHP, MYSQL,
        and JavaScript.</p>

        <p>I'm looking forward to seeing the site mature, and I hope that you'll check back from time-to-time
        to see the progress that's being made!</p>
       

